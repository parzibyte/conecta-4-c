#include "conecta4.h"
#include <stdlib.h> // rand y RAND_MAX

char obtenerOponente(char jugador) {
    if (jugador == JUGADOR_1) {
        return JUGADOR_2;
    } else {
        return JUGADOR_1;
    }
}


int aleatorio_en_rango(int minimo, int maximo) {
    return minimo + rand() / (RAND_MAX / (maximo - minimo + 1) + 1);
}